from django.contrib import admin
from django.urls import path
from rest_framework import routers
from apps.security.views import SignInAPI, user_info, RegisterUserAPI

from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

router = routers.DefaultRouter()

schema_view = get_schema_view(
   openapi.Info(
      title="API Documentation",
      default_version='v1',
      description="API Test description",
      contact=openapi.Contact(email="macurandb@gmail.com"),
      license=openapi.License(name="GPL License"),
   ),
   public=True,
   permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/v1/signin', SignInAPI.as_view(), name="sign-in"),
    path('api/v1/user_info', user_info),
    path('api/v1/register', RegisterUserAPI.as_view(), name="register-user"),
    path('api/docs/swagger.json', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('api/v1/docs/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('api/v1/redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
]

urlpatterns += router.urls
